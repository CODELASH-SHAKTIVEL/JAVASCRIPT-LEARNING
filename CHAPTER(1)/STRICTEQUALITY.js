// Loose Equality (==) Vs Strict Equality Operator(===)


// Loose Equality Operator 
let age = 22; // number type value 
console.log(age == "22"); // Output: true 
// focus Only On Value But Not type 


// Strict Equality Operator
console.log(age === "22") // Output : false 
// Focus on Both Value and its type 
console.log(age !=="22")
