// ARROW FUNCTION

// SPECIAL FORM OF FUNCTION EXPRESSION 
// IT ALLOWS US TO WRITE FUNCTION MORE FAST BECAUSE
// NO NEED TO USE FUNCTION KEYWORD 
// NO NEED TO USE () KEYWORD 
// NO NEED TO USE {} KEYWORD FOR ONE LINE 
//  NO NEED TO USE RETURN STATEMENT IF SINGLE LINE CODE IN FUNCTION 


// FUNCTION EXPRESSION 
let invitiation = function(name){
    console.log(`Hi ${name}, Welcome to my party!`);
}
invitiation("John"); // Output: Hi John, welcome to my party!


// ARROW FUNCTION
let greetingsArrowFunction= (name) => `HELLO MY NAME IS ${name}`
console.log(greetingsArrowFunction("HARRY"));